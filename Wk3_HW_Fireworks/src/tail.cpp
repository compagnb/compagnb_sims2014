#include "tail.h"

//-------------------------------------------------------------------------------------
//constructor
//-------------------------------------------------------------------------------------

void tail::setup(){
    tailCount = 0;
}

//-------------------------------------------------------------------------------------
// update
//-------------------------------------------------------------------------------------

void tail::update(float x, float y){
    temp.x = x;
    temp.y = y;
}

//-------------------------------------------------------------------------------------
//DRAW!!
//-------------------------------------------------------------------------------------
void tail::draw(){
    tailCount ++;
    points.push_back(temp);
    if (points.size() >40){
        points.erase(points.begin());
    }
    ofNoFill();
    ofBeginShape();
    for (int i = 0; i < points.size(); i++) {
        ofVertex(points[i].x, points[i].y);
    }
    ofEndShape();
    
}
//-------------------------------------------------------------------------------------
//Clear Points
//-------------------------------------------------------------------------------------
void tail::clearPoints(){
    points.clear();
    tailCount = 0;
}
